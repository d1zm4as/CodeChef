#include <stdio.h>

int main(void) {
int t; 
scanf("%d",&t); 
while(t--)
{ 
   int x; 
   scanf("%d",&x); 
   
   int c; 
   if(x%4==0)
   { 
     c=x/4; 
   } 
   else{ 
     c=x/4+1;  
       
       
   } 
   printf("%d\n",c);
       
   
   
    
    
    
    
}
}
